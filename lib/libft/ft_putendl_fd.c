#include "libft.h"
#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		if (write(fd, &s[i], sizeof(*s)))
			i++;
	}
	if (write(fd, "\n", sizeof(char)))
		return ;
}
